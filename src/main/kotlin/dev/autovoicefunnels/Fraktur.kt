private fun fraktur(state: String): String {
    val sb = StringBuilder()
    state.forEach { char ->
        sb.append(
            frakturMap[char] ?: char
        )
    }
    return sb.toString()
}

val frakturedStates: List<String> = statesOfTheUSofA.map { state -> fraktur(state) }
